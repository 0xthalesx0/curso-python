"""
+ - adição
- - subtração
* - multiplicação
/ - divisão
//- divisão inteira
**- exponenciação
% - módulo/resto
()- ordem
"""

print(10 * 10)
print(10 + 10)
print(10 - 5)
print(10 / 10)
print('\n')

print('10' * 10)  # repetição de string
print('10' + '10')  # concatenação
print('Luiz Otávio tem ' + str(32) + ' anos')  # conversão str

print(10 // 3)
print(10 ** 10)
print(10 % 3)
